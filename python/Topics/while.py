# About while loops

# printing a list of numbers one by one in new line 

x  = 1

z = int(input("Enter the number to be printed till in the series: "))
while x <= z:
    print(x)
    x += 1


print("No more")

# The while loop works in this way;
# the variable z is checked for condition specified with relation to x
# It constantly checks everything in the condition until the condition is false one
