// A program to understand loops 
# include <stdio.h>

void main(){
    int num = 0;
    
    /* while loop-  will execute the instructions in the loop till the condition in the 
     parenthesis is true
     */
    while( num <= 20){
        printf("%d\n", num);
        // Increment operator, adds +1 with every iteration
        num++;

    }
}